personal_code = input("Введите персональный код (11 символов): ")

# Проверим длину персонального кода
if len(personal_code) != 11:
    print("Ошибка: персональный код должен состоять из 11 символов.")
else:
    # Проверим, что все символы являются цифрами
    if not personal_code.isdigit():
        print("Ошибка: персональный код должен состоять только из цифр.")
    else:
        # Определим пол человека по первой цифре персонального кода
        first_digit = int(personal_code[0])
        if first_digit % 2 == 0:
            gender = "мужчина"
        else:
            gender = "женщина"

        print("Пол человека определен как:", gender)


