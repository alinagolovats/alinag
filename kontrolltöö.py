total_participants = int(input("Введите общее количество участников: "))

# Предполагаем, что в каждой комнате проживает по 2 участника
участников_в_комнате = 2

# Рассчитываем количество номеров, которые необходимо забронировать
количество_номеров = total_participants // участников_в_комнате

# Определяем количество мальчиков и девочек
# Предположим, что у нас есть два списка с полом участников: 'M' - мальчик, 'F' - девочка
# Программа должна подсчитать количество 'M' и 'F' и разделить их поровну на количество номеров
пол_участников = ['M', 'F', 'M', 'F', 'M', 'F']  # Пример списка с полом участников
количество_мальчиков = пол_участников.count('M')
количество_девочек = пол_участников.count('F')

# Рассчитываем сколько мальчиков и девочек поместится в каждом номере
мальчиков_в_комнате = количество_мальчиков // количество_номеров
девочек_в_комнате = количество_девочек // количество_номеров

print("Необходимо забронировать", количество_номеров, "номеров.")
print("В каждом номере будут проживать:")
print("Мальчиков:", мальчиков_в_комнате)
print("Девочек:", девочек_в_комнате)


