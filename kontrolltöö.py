количество_пользователей = int(input("Введите количество персональных кодов для проверки: "))
мужчины = 0
женщины = 0

for i in range(количество_пользователей):
    персональный_код = input("Введите персональный код (11 символов): ")
    if len(персональный_код) != 11:
        print("Неверный формат персонального кода.")
        continue

    пол = int(персональный_код[0])
    if пол % 2 == 0:
        женщины += 1
    else:
        мужчины += 1

print(f"Из {количество_пользователей} человек {мужчины} мужчин и {женщины} женщин.")
