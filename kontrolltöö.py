башня = [
    "  ^  ",
    " / / ",
    " \ \ ",
    " / /_"]

print("Введите число башен от 1 до 9: ")
число_башен = int(input())

if 1 <= число_башен <= 9:
    for line in башня:
        for i in range(число_башен):
            print(line, end='  ')
        print()
else:
    print("Неверный ввод. Введите число от 1 до 9.")


