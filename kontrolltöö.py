вес = float(input("Введите ваш вес в килограммах: "))
рост = float(input("Введите ваш рост в метрах: "))
возраст = int(input("Введите ваш возраст в годах: "))

# Рассчитываем индекс массы тела (ИМТ)
ИМТ = вес / (рост ** 2)

# Определяем характеристику ИМТ
if ИМТ < 18.5:
    характеристика = "недостаточной массой тела"
elif ИМТ < 25:
    характеристика = "нормальной массой тела"
elif ИМТ < 30:
    характеристика = "избыточной массой тела (предожирение)"
else:
    характеристика = "ожирением"

# Выводим результат
print("Ваш индекс массы тела (ИМТ) составляет:", round(ИМТ, 2))
print("Ваш ИМТ характеризуется как:", характеристика)


