вес = float(input("Введите ваш вес: "))
рост = float(input("Введите ваш рост: "))/100
возраст = int(input("Введите ваш возраст: "))

ИМТ = вес / (рост * рост)

if ИМТ < 18.5:
    характеристика = "недостаточной массой тела"
elif ИМТ < 25:
    характеристика = "нормальной массой тела"
elif ИМТ < 30:
    характеристика = "избыточной массой тела (предожирение)"
else:
    характеристика = "ожирением"

print("Ваш индекс массы тела (ИМТ) составляет:", round(ИМТ, 2))
print("Ваш ИМТ характеризуется как:", характеристика)


